        Config.CurrentTarget = nil
        if Config.AimbotEnabled then
            StatusLabel.Text = "рҹҹў аёҒаёіаёҘаёұаёҮаё—аёіаёҮаёІаёҷ"
            StatusLabel.TextColor3 = Color3.fromRGB(0, 255, 100)
