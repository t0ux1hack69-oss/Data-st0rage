local function Notify(title, text, duration)
    pcall(function()
        StarterGui:SetCore("SendNotification", {
            Title = title,
            Text = text,
            Duration = duration or 3
        })
    end)
end

local function GetDistance(pos1, pos2)
    return (pos1 - pos2).Magnitude
end

local function GetCharacter()
    return LocalPlayer.Character
end

local function GetHumanoidRootPart()
    local char = GetCharacter()
    if char then
        return char:FindFirstChild("HumanoidRootPart")
    end
    return nil
end

local function IsPlayerAlive(player)
    if not player or player == LocalPlayer then return false end
    local char = player.Character
    if not char then return false end
    local humanoid = char:FindFirstChildOfClass("Humanoid")
    if not humanoid then return false end
    return humanoid.Health > 0
end

local function GetFightEvent()
    local char = GetCharacter()
    if not char then return nil end
    local fight = char:FindFirstChild("Fight")
    if not fight then return nil end
    local event = fight:FindFirstChild("FightEvent")
    return event
end

local function FireAttack(moveName)
    local event = GetFightEvent()
    if event then
        pcall(function()
            event:FireServer(moveName)
        end)
    end
end

local function TeleportToPosition(targetPos)
    local hrp = GetHumanoidRootPart()
    if hrp and targetPos then
        pcall(function()
            hrp.CFrame = CFrame.new(targetPos)
        end)
    end
end

local function TeleportToPart(part)
    if part and part:IsA("BasePart") then
        TeleportToPosition(part.Position + Vector3.new(0, 3, 0))
    end
end

