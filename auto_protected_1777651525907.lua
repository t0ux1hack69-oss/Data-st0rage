local function GetClosestPlayer()
    local myHRP = GetHumanoidRootPart()
    if not myHRP then return nil end

    local closestPlayer = nil
    local closestDist = _G.AttackRange

    for _, player in ipairs(TargetPlayers) do
        if IsPlayerAlive(player) and player.Character then
            local targetHRP = player.Character:FindFirstChild("HumanoidRootPart")
            if targetHRP then
                local dist = GetDistance(myHRP.Position, targetHRP.Position)
                if dist < closestDist then
                    closestDist = dist
                    closestPlayer = player
                end
            end
        end
    end

    return closestPlayer
end

local function PerformAutoAttack()
    local currentTime = tick()
    if currentTime - LastAttackTime < _G.AttackDelay then return end

    local target = GetClosestPlayer()
    if not target then return end

    local move = AttackMoves[CurrentAttackIndex]
    FireAttack(move)

    CurrentAttackIndex = CurrentAttackIndex + 1
    if CurrentAttackIndex > #AttackMoves then
        CurrentAttackIndex = 1
    end

    LastAttackTime = currentTime
end

