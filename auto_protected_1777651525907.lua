local function GetFarmZone()
    local container = Workspace:FindFirstChild("ZonePlusServerContainer")
    if container then
        return container:FindFirstChild("ZonePlusCheckerPart")
    end
    return nil
end

local function PerformAutoFarm()
    local currentTime = tick()
    if currentTime - LastFarmTime < _G.FarmInterval then return end

    local zone = GetFarmZone()
    if zone then
        TeleportToPart(zone)
    end

    LastFarmTime = currentTime
end

