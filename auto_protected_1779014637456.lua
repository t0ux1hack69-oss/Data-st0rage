local function getRemoteFunction()
    local pathParts = string.split(CONFIG.REMOTE_PATH, ".")
    local current = ReplicatedStorage
    for _, part in ipairs(pathParts) do
        current = current:FindFirstChild(part)
        if not current then
            warn("[а№ҖаёҠаёҙаёҹаёһаёұаёҮ] а№„аёЎа№Ҳаёһаёҡ Remote: " .. CONFIG.REMOTE_PATH)
            return nil
        end
    end
    return current
end

local function invokeRemote(settingValue)
    local remote = getRemoteFunction()
    if not remote then return false end

    local success, result = pcall(function()
        return remote:InvokeServer("ChangeSetting", CONFIG.SETTING_NAME, settingValue)
    end)

    if not success then
        warn("[а№ҖаёҠаёҙаёҹаёһаёұаёҮ] Remote Error: " .. tostring(result))
        return false
    end

    return true
end

local function createTween(instance, properties, duration, easingStyle, easingDirection)
    easingStyle = easingStyle or Enum.EasingStyle.Quad
    easingDirection = easingDirection or Enum.EasingDirection.Out
    duration = duration or 0.3

    local success, tween = pcall(function()
        return TweenService:Create(
            instance,
            TweenInfo.new(duration, easingStyle, easingDirection),
            properties
        )
    end)

    if success and tween then
        return tween
    end
    return nil
end

local function safeTweenPlay(tween)
    if tween and typeof(tween) == "Instance" and tween:IsA("Tween") then
        pcall(function() tween:Play() end)
    end
end

local function makeDraggable(frame, handle)
    local dragging = false
    local dragStart = nil
    local startPos = nil

    handle.InputBegan:Connect(function(input)
        if input.UserInputType == Enum.UserInputType.MouseButton1 or input.UserInputType == Enum.UserInputType.Touch then
            dragging = true
            dragStart = input.Position
            startPos = frame.Position
        end
    end)

    handle.InputChanged:Connect(function(input)
        if dragging and (input.UserInputType == Enum.UserInputType.MouseMovement or input.UserInputType == Enum.UserInputType.Touch) then
            local delta = input.Position - dragStart
            frame.Position = UDim2.new(
                startPos.X.Scale, startPos.X.Offset + delta.X,
                startPos.Y.Scale, startPos.Y.Offset + delta.Y
            )
        end
    end)

    UserInputService.InputEnded:Connect(function(input)
        if input.UserInputType == Enum.UserInputType.MouseButton1 or input.UserInputType == Enum.UserInputType.Touch then
            dragging = false
        end
    end)
end

