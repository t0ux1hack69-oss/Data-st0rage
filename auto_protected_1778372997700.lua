--// Configuration
local Config = {
    Enabled = false,
    Mode = "Nearest",
    WallCheck = true,
    TeamCheck = true,
    MaxDistance = 1000,
    Smoothness = 0.15,
    AimPart = "Head",
    Keybind = Enum.KeyCode.E,
    UIKeybind = Enum.KeyCode.Insert,
    RainbowSpeed = 0.5,
}

