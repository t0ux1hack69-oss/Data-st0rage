-- в•җв•җв•җв•җв•җв•җв•җ MAIN FIRE SEQUENCE в•җв•җв•җв•җв•җв•җв•җ

-- Full fire sequence at target
local function FireAtTarget(targetData)
    if not targetData then return end

    local targetPos = targetData.Position
    local targetInstance = targetData.Instance
    local isPlayer = targetData.IsPlayer

    if not targetPos then return end

    State.IsFiring = true
    State.LastFire = tick()
    State.CurrentTarget = targetInstance

    -- Update session ID for each shot
    State.SessionId = GenerateSessionId()
    State.HitId = GenerateHitId()

    -- Step 1: Aim at target
    local aimCFrame = CalculateAimCFrame(targetPos)
    if aimCFrame then
        SmoothAim(aimCFrame)
    end

    -- Small delay for aim
    task.wait(0.02)

    -- Step 2: Fire CreateStayProjectile (visual projectile)
    FireCreateStayProjectile(targetPos, targetInstance)

    task.wait(0.03)

    -- Step 3: Fire BulletEvent (bullet registration)
    FireBulletEvent(targetPos)

    task.wait(0.05)

    -- Step 4: Fire ValidateHit (damage application)
    if isPlayer then
        FireValidateHitPlayer(targetPos, targetInstance)
    else
        local collider = nil
        if targetInstance:IsA("Model") then
            collider = targetInstance:FindFirstChild("_Collider")
        end
        FireValidateHit(targetPos, targetInstance, collider)
    end

    State.IsFiring = false

    if Config.ShowDebug then
        print("[вң…] Full sequence completed | Target:", isPlayer and targetInstance.Name or targetInstance.Name)
    end
end

