-- Calculate aim CFrame towards target
local function CalculateAimCFrame(targetPos)
    if not targetPos then return nil end

    local startPos = HumanoidRootPart.Position
    local direction = (targetPos - startPos).Unit

    -- Create CFrame looking at target
    local lookCFrame = CFrame.lookAt(startPos, targetPos)

    return lookCFrame
end

-- Smooth aim (optional)
local function SmoothAim(targetCFrame)
    if not targetCFrame then return end

    if Config.AimbotSmooth then
        -- Smooth interpolation would go here
        -- For now, instant snap
        HumanoidRootPart.CFrame = CFrame.new(HumanoidRootPart.Position) * CFrame.Angles(0, math.atan2(targetCFrame.LookVector.X, targetCFrame.LookVector.Z), 0)
    else
        -- Instant snap to target direction
        local currentPos = HumanoidRootPart.Position
        local lookVector = targetCFrame.LookVector
        HumanoidRootPart.CFrame = CFrame.lookAt(currentPos, currentPos + lookVector)
    end
end

