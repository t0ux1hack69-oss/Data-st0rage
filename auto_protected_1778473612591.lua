-- Get current bow tool
local function GetBow()
    local char = LocalPlayer.Character
    if not char then return nil end

    -- Check if bow is already equipped
    for _, tool in ipairs(char:GetChildren()) do
        if tool:IsA("Tool") and tool.Name == Config.BowName then
            return tool
        end
    end

    -- Check backpack
    local backpack = LocalPlayer:FindFirstChild("Backpack")
    if backpack then
        for _, tool in ipairs(backpack:GetChildren()) do
            if tool:IsA("Tool") and tool.Name == Config.BowName then
                if Config.AutoEquipBow then
                    tool.Parent = char
                    task.wait(0.1)
                    return tool
                end
                return tool
            end
        end
    end

    return nil
end

-- Get character's HumanoidRootPart
local function GetHRP(target)
    if not target then return nil end

    if target:IsA("Model") then
        return target:FindFirstChild("HumanoidRootPart") or target:FindFirstChild("Torso") or target:FindFirstChild("UpperTorso")
    elseif target:IsA("BasePart") then
        return target
    end

    return nil
end

-- Get target position (with headshot priority for players)
local function GetTargetPosition(target, isPlayer)
    if not target then return nil end

    if isPlayer and target.Character then
        local char = target.Character
        if Config.HeadshotPriority then
            local head = char:FindFirstChild("Head")
            if head then
                return head.Position
            end
        end
        local hrp = char:FindFirstChild("HumanoidRootPart")
        if hrp then
            return hrp.Position
        end
    elseif not isPlayer then
        -- NPC
        if target:IsA("Model") then
            local hrp = target:FindFirstChild("HumanoidRootPart")
            if hrp then return hrp.Position end

            local collider = target:FindFirstChild("_Collider")
            if collider then return collider.Position end

            -- Fallback to primary part or any part
            if target.PrimaryPart then
                return target.PrimaryPart.Position
            end
            for _, part in ipairs(target:GetDescendants()) do
                if part:IsA("BasePart") then
                    return part.Position
                end
            end
        elseif target:IsA("BasePart") then
            return target.Position
        end
    end

    return nil
end

-- Calculate distance
local function GetDistance(pos1, pos2)
    if not pos1 or not pos2 then return math.huge end
    return (pos1 - pos2).Magnitude
end

-- Get all valid NPCs
local function GetNPCs()
    local npcs = {}
    local npcFolder = Workspace:FindFirstChild("NPCS")
    if not npcFolder then return npcs end

    for _, npc in ipairs(npcFolder:GetChildren()) do
        if npc:IsA("Model") or npc:IsA("BasePart") then
            local pos = GetTargetPosition(npc, false)
            if pos then
                local distance = GetDistance(HumanoidRootPart.Position, pos)
                if distance <= Config.MaxDistance then
                    table.insert(npcs, {Instance = npc, Distance = distance, Position = pos, IsPlayer = false})
                end
            end
        end
    end

    return npcs
end

-- Get all valid Players (enemies)
local function GetPlayers()
    local targets = {}

    for _, player in ipairs(Players:GetPlayers()) do
        if player ~= LocalPlayer and player.Character then
            local char = player.Character
            local hrp = char:FindFirstChild("HumanoidRootPart")
            local humanoid = char:FindFirstChild("Humanoid")

            if hrp and humanoid and humanoid.Health > 0 then
                local pos = hrp.Position
                local distance = GetDistance(HumanoidRootPart.Position, pos)
                if distance <= Config.MaxDistance then
                    table.insert(targets, {Instance = player, Distance = distance, Position = pos, IsPlayer = true})
                end
            end
        end
    end

    return targets
end

-- Get nearest target based on mode
local function GetNearestTarget()
    local allTargets = {}

    if Config.TargetMode == "NPC" or Config.TargetMode == "Both" then
        local npcs = GetNPCs()
        for _, npc in ipairs(npcs) do
            table.insert(allTargets, npc)
        end
    end

    if Config.TargetMode == "Player" or Config.TargetMode == "Both" then
        local players = GetPlayers()
        for _, player in ipairs(players) do
            table.insert(allTargets, player)
        end
    end

    -- Sort by distance (nearest first)
    table.sort(allTargets, function(a, b)
        return a.Distance < b.Distance
    end)

    return allTargets[1] -- Return nearest target
end

-- Generate random session ID
local function GenerateSessionId()
    return "KlDC00I_" .. tostring(math.random(3330000, 3339999))
end

-- Generate hit ID
local function GenerateHitId()
    local chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
    local result = ""
    for i = 1, 6 do
        local rand = math.random(1, #chars)
        result = result .. chars:sub(rand, rand)
    end
    return result
end

