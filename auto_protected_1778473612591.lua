-- Main auto-fire loop
local connection
connection = RunService.Heartbeat:Connect(function()
    if not Config.Enabled then return end
    if State.IsFiring then return end

    -- Check fire rate
    if tick() - State.LastFire < Config.FireRate then return end

    -- Update character reference
    if not LocalPlayer.Character then return end
    if not HumanoidRootPart then
        HumanoidRootPart = LocalPlayer.Character:FindFirstChild("HumanoidRootPart")
        if not HumanoidRootPart then return end
    end

    -- Check if bow is equipped
    local bow = GetBow()
    if not bow then
        if Config.AutoEquipBow then
            -- Try to equip from backpack
            local backpack = LocalPlayer:FindFirstChild("Backpack")
            if backpack then
                for _, tool in ipairs(backpack:GetChildren()) do
                    if tool:IsA("Tool") and tool.Name == Config.BowName then
                        tool.Parent = LocalPlayer.Character
                        task.wait(0.1)
                        bow = tool
                        break
                    end
                end
            end
        end
        if not bow then return end
    end

    -- Get nearest target
    local target = GetNearestTarget()
    if not target then return end

    -- Fire at target
    FireAtTarget(target)
end)

