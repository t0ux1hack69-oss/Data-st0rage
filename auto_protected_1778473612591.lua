-- Fire CreateStayProjectile remote
local function FireCreateStayProjectile(targetPos, targetInstance)
    if not targetPos then return end

    local bow = GetBow()
    if not bow then return end

    local firePoint = bow:FindFirstChild("Handle", true)
    if firePoint then
        firePoint = firePoint:FindFirstChild("FirePoint")
    end

    local startPos = HumanoidRootPart.Position
    local endPos = targetPos
    local direction = (endPos - startPos).Unit
    local distance = (endPos - startPos).Magnitude

    -- Calculate mid-point for trajectory
    local midPoint = startPos + direction * (distance * 0.5)
    midPoint = midPoint + Vector3.new(0, 2, 0) -- Slight arc

    -- Generate CFrames
    local startCFrame = CFrame.lookAt(startPos, endPos)
    local midCFrame = CFrame.lookAt(midPoint, endPos)
    local endCFrame = CFrame.lookAt(endPos, endPos + direction)

    -- Find a block to "hit" (for the 4th argument)
    local hitBlock = nil
    local blocksFolder = Workspace:FindFirstChild("Blocks")
    if blocksFolder then
        local castle = blocksFolder:FindFirstChild("Folder")
        if castle then
            castle = castle:FindFirstChild("Castle")
            if castle then
                local breakFolder = castle:FindFirstChild("Break")
                if breakFolder then
                    local children = breakFolder:GetChildren()
                    if #children > 0 then
                        hitBlock = children[math.random(1, #children)]
                    end
                end
            end
        end
    end

    -- Fallback hit block
    if not hitBlock then
        hitBlock = Workspace:FindFirstChildWhichIsA("BasePart", true)
    end

    local args = {
        [1] = "Bow",
        [2] = State.SessionId,
        [3] = "Arrow",
        [4] = hitBlock,
        [5] = State.SessionId,
        [6] = startCFrame,
        [7] = midCFrame,
        [8] = endCFrame,
    }

    pcall(function()
        Remotes.CreateStayProjectile:FireServer(unpack(args))
    end)

    if Config.ShowDebug then
        print("[рҹ”Ҙ] CreateStayProjectile fired | Target:", tostring(targetInstance), "| Distance:", math.floor(distance))
    end
end

-- Fire BulletEvent remote
local function FireBulletEvent(targetPos)
    if not targetPos then return end

    local bow = GetBow()
    if not bow then return end

    local handle = bow:FindFirstChild("Handle")
    if not handle then return end

    local firePoint = handle:FindFirstChild("FirePoint")
    local shellEject = handle:FindFirstChild("ShellEjectPoint")
    local fireSound = firePoint and firePoint:FindFirstChild("FireSound")
    local hitSound = firePoint and firePoint:FindFirstChild("HitSound")

    local startPos = HumanoidRootPart.Position
    local endPos = targetPos
    local direction = (endPos - startPos).Unit

    local fireCFrame = CFrame.lookAt(startPos, endPos)

    local args = {
        [1] = "Bow",
        [2] = bow,
        [3] = endPos,
        [4] = fireCFrame,
        [5] = direction,
        [6] = Config.ProjectileSpeed,
        [7] = firePoint or handle,
        [8] = false,
        [9] = shellEject or handle,
        [10] = fireSound,
        [11] = hitSound,
        [12] = State.SessionId,
        [13] = 1,
        [14] = 1,
    }

    pcall(function()
        Remotes.BulletEvent:FireServer(unpack(args))
    end)

    if Config.ShowDebug then
        print("[рҹ’Ҙ] BulletEvent fired | Pos:", tostring(endPos))
    end
end

-- Fire ValidateHit remote (for NPCs)
local function FireValidateHit(targetPos, targetInstance, targetCollider)
    if not targetPos or not targetInstance then return end

    local bow = GetBow()
    if not bow then return end

    local handle = bow:FindFirstChild("Handle")
    local firePoint = handle and handle:FindFirstChild("FirePoint")
    local hitSound = firePoint and firePoint:FindFirstChild("HitSound")

    -- Get collider
    local collider = targetCollider
    if not collider and targetInstance:IsA("Model") then
        collider = targetInstance:FindFirstChild("_Collider")
    end
    if not collider then
        collider = targetInstance
    end

    -- Calculate hit position (on the target)
    local hitPos = targetPos
    if collider and collider:IsA("BasePart") then
        hitPos = collider.Position
    end

    -- Start position (from player)
    local startPos = HumanoidRootPart.Position

    -- Impact position (slightly offset from hit)
    local impactPos = hitPos + Vector3.new(
        math.random(-1, 1) * 0.5,
        math.random(-1, 1) * 0.5,
        math.random(-1, 1) * 0.5
    )

    local args = {
        [1] = startPos,
        [2] = collider,
        [3] = impactPos,
        [4] = "Bow",
        [5] = {
            ["BloodData"] = {
                ["Enabled"] = true,
                ["Blood1"] = true,
            },
            ["Type"] = "Ranged",
            ["Knockback"] = Config.KnockbackOverride,
            ["KnockbackTime"] = 0.1,
            ["Progress"] = 1,
            ["HitEffectsForMe"] = true,
            ["Sound"] = hitSound,
            ["Damage"] = Config.DamageOverride,
        },
        [6] = GenerateSessionId(),
        [7] = State.HitId,
    }

    pcall(function()
        Remotes.ValidateHit:FireServer(unpack(args))
    end)

    if Config.ShowDebug then
        print("[вҡЎ] ValidateHit fired | NPC:", targetInstance.Name, "| Damage:", Config.DamageOverride)
    end
end

