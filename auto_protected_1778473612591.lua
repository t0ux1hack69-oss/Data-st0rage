    if gameProcessed then return end
    if input.KeyCode == Enum.KeyCode.RightShift then
        Config.Enabled = not Config.Enabled
        print("[рҹҸ№] Auto Bow:", Config.Enabled and "вң… ENABLED" or "вқҢ DISABLED")
    end

    -- Toggle target mode with Insert
    if input.KeyCode == Enum.KeyCode.Insert then
        if Config.TargetMode == "NPC" then
            Config.TargetMode = "Player"
        elseif Config.TargetMode == "Player" then
            Config.TargetMode = "Both"
        else
            Config.TargetMode = "NPC"
        end
        print("[рҹҺҜ] Target Mode:", Config.TargetMode)
    end
end)

