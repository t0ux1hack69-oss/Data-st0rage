                            end
                        end
                        local step = (_G.V2Mode == "2 กระบอก") and 2 or 1
                        for i = 1, #guns, step do
                            if (not _G.V2Shoot and not _G.KillAllEnabled) or _G.IsRecovering or _G.IsDead then break end
                            forceUnequip()
                            local g1 = guns[i]
