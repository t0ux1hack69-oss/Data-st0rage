local ActiveParticles = {}

local function CreateNeonParticle(originPosition, parent)
    local particle = Instance.new("Frame")
    particle.Name = "NeonParticle"
    particle.BorderSizePixel = 0
    particle.BackgroundColor3 = math.random() > 0.5 and CONFIG.NEON_RED or CONFIG.NEON_PINK
    particle.Size = UDim2.new(0, math.random(3, 6), 0, math.random(3, 6))
    particle.Position = originPosition
    particle.ZIndex = 100
    particle.Parent = parent

    local glow = Instance.new("ImageLabel")
    glow.Name = "Glow"
    glow.BackgroundTransparency = 1
    glow.Image = "rbxassetid://5028857084"
    glow.ImageColor3 = particle.BackgroundColor3
    glow.ImageTransparency = 0.3
    glow.Size = UDim2.new(2, 0, 2, 0)
    glow.Position = UDim2.new(-0.5, 0, -0.5, 0)
    glow.ZIndex = 99
    glow.Parent = particle

    local angle = math.random() * math.pi * 2
    local distance = math.random(50, CONFIG.PARTICLE_SPEED)
    local velocityX = math.cos(angle) * distance
    local velocityY = math.sin(angle) * distance

    local startTime = tick()
    local lifetime = CONFIG.PARTICLE_LIFETIME + (math.random() * 0.4)

    table.insert(ActiveParticles, {
        Frame = particle,
        StartTime = startTime,
        Lifetime = lifetime,
        StartPos = originPosition,
        Velocity = Vector2.new(velocityX, velocityY),
        RotationSpeed = math.random(-360, 360)
    })

    return particle
end

local function SpawnParticleBurst(originPosition, count, parent)
    count = count or CONFIG.PARTICLE_COUNT
    for i = 1, count do
        CreateNeonParticle(originPosition, parent)
    end
end

RunService.RenderStepped:Connect(function(dt)
    local currentTime = tick()
    for i = #ActiveParticles, 1, -1 do
        local p = ActiveParticles[i]
        local elapsed = currentTime - p.StartTime
        local progress = elapsed / p.Lifetime

        if progress >= 1 then
            p.Frame:Destroy()
            table.remove(ActiveParticles, i)
        else
            local ease = 1 - (1 - progress) * (1 - progress)

            local newX = p.StartPos.X.Offset + (p.Velocity.X * ease)
            local newY = p.StartPos.Y.Offset + (p.Velocity.Y * ease)

            p.Frame.Position = UDim2.new(p.StartPos.X.Scale, newX, p.StartPos.Y.Scale, newY)
            p.Frame.Rotation = p.RotationSpeed * elapsed
            p.Frame.BackgroundTransparency = progress
            p.Frame.Glow.ImageTransparency = 0.3 + (progress * 0.7)
        end
    end
end)

