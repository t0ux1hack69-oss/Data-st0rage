local function SwitchTab(selectedTab)
    if selectedTab == activeTab then return end

    CreateTween(activeTab, {BackgroundColor3 = CONFIG.ACCENT_COLOR}, 0.2):Play()

    activeTab = selectedTab
    CreateTween(selectedTab, {BackgroundColor3 = CONFIG.NEON_RED}, 0.2):Play()

    local absPos = selectedTab.AbsolutePosition
    local absSize = selectedTab.AbsoluteSize
    local tabPos = UDim2.new(0, absPos.X + absSize.X/2, 0, absPos.Y + absSize.Y/2)
    SpawnParticleBurst(tabPos, 8, LMG2L["ScreenGui_1"])

    if selectedTab == LMG2L["Tab1_9"] then
        MainContent.Visible = true
        PlayerContent.Visible = false
    else
        MainContent.Visible = false
        PlayerContent.Visible = true
    end
end

