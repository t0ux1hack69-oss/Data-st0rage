-- Friend Cache
local friendCache = {}
local function isFriend(plr)
    if friendCache[plr.UserId] ~= nil then return friendCache[plr.UserId] end
    local ok, result = pcall(function() return LocalPlayer:IsFriendsWith(plr.UserId) end)
