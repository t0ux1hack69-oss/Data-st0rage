    }
end

local function formatDuration(sec)
    sec = tonumber(sec) or 0
    if sec <= 0 then return "а№„аёЎа№Ҳаё—аёЈаёІаёҡ" end
    return string.format("%d:%02d", math.floor(sec/60), math.floor(sec%60))
end

local function timeAgo(created)
    if not created or created == "" then return "а№„аёЎа№Ҳаё—аёЈаёІаёҡ" end
    local Y,M,D,h,m,s = created:match("(%d+)-(%d+)-(%d+)T(%d+):(%d+):(%d+)")
    if not Y then return "а№„аёЎа№Ҳаё—аёЈаёІаёҡ" end
    local ok, ts = pcall(os.time, {year=tonumber(Y),month=tonumber(M),day=tonumber(D),
        hour=tonumber(h),min=tonumber(m),sec=tonumber(s)})
    if not ok then return "а№„аёЎа№Ҳаё—аёЈаёІаёҡ" end
    local d = math.max(0, os.time()-ts)
    if     d>=31536000 then return math.floor(d/31536000).." аёӣаёөаё—аёөа№Ҳа№ҒаёҘа№үаё§"
    elseif d>=2592000  then return math.floor(d/2592000).." а№Җаё”аё·аёӯаёҷаё—аёөа№Ҳа№ҒаёҘа№үаё§"
    elseif d>=86400    then return math.floor(d/86400).." аё§аёұаёҷаё—аёөа№Ҳа№ҒаёҘа№үаё§"
    elseif d>=3600     then return math.floor(d/3600).." аёҠаёұа№Ҳаё§а№ӮаёЎаёҮаё—аёөа№Ҳа№ҒаёҘа№үаё§"
    elseif d>=60       then return math.floor(d/60).." аёҷаёІаё—аёөаё—аёөа№Ҳа№ҒаёҘа№үаё§"
    else return "а№ҖаёЎаё·а№ҲаёӯаёҒаёөа№үаёҷаёөа№ү" end
end

