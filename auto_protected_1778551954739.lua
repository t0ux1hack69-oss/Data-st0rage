local function fetchSongInfo(soundId)
    local sid = tostring(soundId)
    local ok1, r1 = pcall(function()
        return HttpService:JSONDecode(game:HttpGet(
            "https://apis.roblox.com/toolbox-service/v1/items/details?assetIds=" .. sid))
    end)
    if ok1 and r1 and r1.data and r1.data[1] then
        local d = r1.data[1]
        return {
            name     = (d.asset and d.asset.name) or d.name or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            creator  = (d.creator and d.creator.name) or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            created  = (d.asset and d.asset.createdAt) or nil,
            duration = (d.asset and d.asset.audioDetails and d.asset.audioDetails.duration) or 0,
        }
    end
    local ok2, r2 = pcall(function()
        return HttpService:JSONDecode(game:HttpGet(
            "https://economy.roblox.com/v2/assets/" .. sid .. "/details"))
    end)
    if ok2 and r2 and r2.Name then
        return {
            name     = r2.Name or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            creator  = (r2.Creator and r2.Creator.Name) or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            created  = r2.Created or nil,
            duration = r2.Duration or 0,
        }
    end
    local ok3, r3 = pcall(function()
        return HttpService:JSONDecode(game:HttpGet(
            "https://www.roblox.com/marketplace/productinfo?assetId=" .. sid))
    end)
    if ok3 and r3 and r3.Name then
        return {
            name     = r3.Name or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            creator  = (r3.Creator and r3.Creator.Name) or "а№„аёЎа№Ҳаё—аёЈаёІаёҡ",
            created  = r3.Created or nil,
            duration = r3.Duration or 0,
