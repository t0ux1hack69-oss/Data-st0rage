local function loadData()
    if isfile(SAVE_FILE) then
        local ok, res = pcall(function() return HttpService:JSONDecode(readfile(SAVE_FILE)) end)
        if ok and type(res) == "table" then savedIds = res end
    end
    if isfile(TAG_FILE) then
        local ok, res = pcall(function() return HttpService:JSONDecode(readfile(TAG_FILE)) end)
        if ok and type(res) == "table" then songTags = res end
    end
end

local function saveData()
    pcall(function() writefile(SAVE_FILE, HttpService:JSONEncode(savedIds)) end)
end

local function saveTags()
