local ConfigSystem = {}

function ConfigSystem:Save()
    local success = pcall(function()
        local configString = HttpService:JSONEncode(Config)
        if writefile then
            writefile("ApexAimbot_Config.json", configString)
        end
    end)
    if success then
        Utility:Notify("рҹ’ҫ Config Saved", "Settings saved successfully", 2)
    end
end

function ConfigSystem:Load()
    local success = pcall(function()
        if readfile and isfile and isfile("ApexAimbot_Config.json") then
            local configString = readfile("ApexAimbot_Config.json")
            local loadedConfig = HttpService:JSONDecode(configString)
            for section, settings in pairs(loadedConfig) do
                if Config[section] then
                    for key, value in pairs(settings) do
                        if Config[section][key] ~= nil then
                            Config[section][key] = value
                        end
                    end
                end
            end
            State.ConfigLoaded = true
            Utility:Notify("рҹ“Ӯ Config Loaded", "Settings loaded from file", 2)
        end
    end)
end

