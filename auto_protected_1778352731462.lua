-- UTILITY FUNCTIONS
local Utility = {}

function Utility:Round(num, decimals)
    decimals = decimals or 0
    local mult = 10 ^ decimals
    return math.floor(num * mult + 0.5) / mult
end

function Utility:GetCharacter(player)
    return player and player.Character
end

function Utility:GetHumanoid(character)
    return character and character:FindFirstChildOfClass("Humanoid")
end

function Utility:GetHead(character)
    return character and character:FindFirstChild("Head")
end

function Utility:GetHRP(character)
    return character and character:FindFirstChild("HumanoidRootPart")
end

function Utility:IsAlive(character)
    local humanoid = self:GetHumanoid(character)
    return humanoid and humanoid.Health > 0
end

function Utility:IsKnocked(character)
    local humanoid = self:GetHumanoid(character)
    return humanoid and humanoid.Health > 0 and humanoid.Health < humanoid.MaxHealth * 0.1
end

function Utility:IsTeammate(player)
    if not Config.Aimbot.TeamCheck then return false end
    return player and player.Team == LocalPlayer.Team
end

function Utility:GetDistance(pos1, pos2)
    return (pos1 - pos2).Magnitude
end

function Utility:WorldToScreen(position)
    local screenPos, onScreen = Camera:WorldToViewportPoint(position)
    return Vector2.new(screenPos.X, screenPos.Y), onScreen, screenPos.Z
end

function Utility:IsVisible(targetPart, origin)
    origin = origin or Camera.CFrame.Position
    if not targetPart then return false end
    local raycastParams = RaycastParams.new()
    raycastParams.FilterDescendantsInstances = {LocalPlayer.Character, targetPart.Parent}
    raycastParams.FilterType = Enum.RaycastFilterType.Blacklist
    raycastParams.IgnoreWater = true
    local direction = (targetPart.Position - origin).Unit
    local distance = (targetPart.Position - origin).Magnitude
    local result = Workspace:Raycast(origin, direction * distance, raycastParams)
    if result then return false end
    return true
end

function Utility:GetClosestPlayer()
    local closestPlayer = nil
    local closestScreenDistance = math.huge
    local mousePos = Vector2.new(Mouse.X, Mouse.Y)
    local cameraPos = Camera.CFrame.Position
    for _, player in ipairs(Players:GetPlayers()) do
        if player == LocalPlayer then continue end
        local character = self:GetCharacter(player)
        if not character then continue end
        if Config.Aimbot.AliveCheck and not self:IsAlive(character) then continue end
        if Config.Aimbot.IgnoreKnocked and self:IsKnocked(character) then continue end
        if Config.Aimbot.TeamCheck and self:IsTeammate(player) then continue end
        local targetPart = character:FindFirstChild(Config.Aimbot.TargetPart)
        if not targetPart then
            targetPart = character:FindFirstChild("Head")
            if not targetPart then continue end
        end
        local screenPos, onScreen, depth = self:WorldToScreen(targetPart.Position)
        if not onScreen or depth < 0 then continue end
        local worldDistance = self:GetDistance(cameraPos, targetPart.Position)
        if worldDistance > Config.Aimbot.DistanceLimit then continue end
        if Config.Aimbot.WallCheck and not self:IsVisible(targetPart) then continue end
        local screenDistance = (screenPos - mousePos).Magnitude
        if screenDistance < closestScreenDistance then
            closestScreenDistance = screenDistance
            closestPlayer = player
        end
    end
    return closestPlayer
end

function Utility:GetClosestPlayerSilent()
    local closestPlayer = nil
    local closestDistance = math.huge
    local mousePos = Vector2.new(Mouse.X, Mouse.Y)
    for _, player in ipairs(Players:GetPlayers()) do
        if player == LocalPlayer then continue end
        local character = self:GetCharacter(player)
        if not character then continue end
        if Config.Aimbot.AliveCheck and not self:IsAlive(character) then continue end
        if Config.SilentAim.TeamCheck and self:IsTeammate(player) then continue end
        local targetPart = character:FindFirstChild(Config.SilentAim.TargetPart)
        if not targetPart then
            targetPart = character:FindFirstChild("Head")
            if not targetPart then continue end
        end
        local screenPos, onScreen, depth = self:WorldToScreen(targetPart.Position)
        if not onScreen or depth < 0 then continue end
        local screenDistance = (screenPos - mousePos).Magnitude
        if screenDistance < closestDistance and screenDistance <= Config.SilentAim.FOV then
            closestDistance = screenDistance
            closestPlayer = player
        end
    end
    return closestPlayer
end

function Utility:PredictPosition(targetPart, player)
    if not Config.Aimbot.Prediction then return targetPart.Position end
    local hrp = self:GetHRP(targetPart.Parent)
    if not hrp then return targetPart.Position end
    local velocity = hrp.Velocity
    local prediction = Config.Aimbot.PredictionAmount
    if Config.Aimbot.Resolver then
        velocity = Vector3.new(
            math.clamp(velocity.X, -500, 500),
            math.clamp(velocity.Y, -500, 500),
            math.clamp(velocity.Z, -500, 500)
        )
        if State.LastVelocity then
            velocity = velocity:Lerp(State.LastVelocity, 0.3)
        end
        State.LastVelocity = velocity
    end
    local pingCompensation = (State.Ping / 1000) * 0.5
    prediction = prediction + pingCompensation
    local gravityCompensation = Vector3.new(0, -Workspace.Gravity * prediction * prediction * 0.5, 0)
    local predictedPos = targetPart.Position + (velocity * prediction) + gravityCompensation
    return predictedPos
end

function Utility:LerpCFrame(cf1, cf2, t)
    return cf1:Lerp(cf2, t)
end

function Utility:RainbowColor(speed)
    speed = speed or 1
    local hue = (tick() * speed) % 1
    return Color3.fromHSV(hue, 1, 1)
end

function Utility:FormatTime()
    local timeTable = os.date("*t")
    return string.format("%02d:%02d:%02d", timeTable.hour, timeTable.min, timeTable.sec)
end

function Utility:Tween(instance, properties, duration, easingStyle, easingDirection)
    duration = duration or 0.3
    easingStyle = easingStyle or Enum.EasingStyle.Quart
    easingDirection = easingDirection or Enum.EasingDirection.Out
    local tweenInfo = TweenInfo.new(duration, easingStyle, easingDirection)
    local tween = TweenService:Create(instance, tweenInfo, properties)
    tween:Play()
    return tween
end

