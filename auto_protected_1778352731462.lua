function ESP:RemoveDrawings(player)
    local drawings = self.Drawings[player]
    if drawings then
        for _, drawing in pairs(drawings) do
            drawing:Remove()
        end
        self.Drawings[player] = nil
    end
end

function ESP:Update()
    if not Config.ESP.Enabled then
        for _, drawings in pairs(self.Drawings) do
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
        end
        return
    end
    
    for player, drawings in pairs(self.Drawings) do
        local character = Utility:GetCharacter(player)
        if not character then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        local humanoid = Utility:GetHumanoid(character)
        local hrp = Utility:GetHRP(character)
        local head = Utility:GetHead(character)
        
        if not humanoid or not hrp or not head then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        if not Utility:IsAlive(character) then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        if Config.ESP.TeamCheck and Utility:IsTeammate(player) then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        local distance = Utility:GetDistance(Camera.CFrame.Position, hrp.Position)
        if distance > Config.ESP.MaxDistance then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        local headPos, headOnScreen = Utility:WorldToScreen(head.Position)
        local hrpPos, hrpOnScreen = Utility:WorldToScreen(hrp.Position)
        
        if not headOnScreen or not hrpOnScreen then
            for _, drawing in pairs(drawings) do
                drawing.Visible = false
            end
            continue
        end
        
        local boxHeight = math.abs(headPos.Y - hrpPos.Y) * 2.5
        local boxWidth = boxHeight * 0.6
        local boxPos = Vector2.new(hrpPos.X - boxWidth / 2, headPos.Y - boxHeight * 0.15)
        
        local boxColor = Config.ESP.RainbowMode and Utility:RainbowColor(1) or Config.ESP.BoxColor
        local tracerColor = Config.ESP.RainbowMode and Utility:RainbowColor(0.8) or Config.ESP.TracerColor
        
        if Config.ESP.Boxes then
            drawings.Box.Size = Vector2.new(boxWidth, boxHeight)
            drawings.Box.Position = boxPos
            drawings.Box.Color = boxColor
            drawings.Box.Thickness = Config.ESP.BoxThickness
            drawings.Box.Visible = true
            
            drawings.BoxOutline.Size = Vector2.new(boxWidth, boxHeight)
            drawings.BoxOutline.Position = boxPos
            drawings.BoxOutline.Thickness = Config.ESP.BoxThickness + 2
            drawings.BoxOutline.Visible = true
        else
            drawings.Box.Visible = false
            drawings.BoxOutline.Visible = false
        end
        
        if Config.ESP.Tracers then
            local origin
            if Config.ESP.TracerOrigin == "Bottom" then
                origin = Vector2.new(Camera.ViewportSize.X / 2, Camera.ViewportSize.Y)
            elseif Config.ESP.TracerOrigin == "Center" then
                origin = Vector2.new(Camera.ViewportSize.X / 2, Camera.ViewportSize.Y / 2)
            elseif Config.ESP.TracerOrigin == "Mouse" then
                origin = Vector2.new(Mouse.X, Mouse.Y)
            end
            
            drawings.Tracer.From = origin
            drawings.Tracer.To = hrpPos
            drawings.Tracer.Color = tracerColor
            drawings.Tracer.Visible = true
            
            drawings.TracerOutline.From = origin
            drawings.TracerOutline.To = hrpPos
            drawings.TracerOutline.Visible = true
        else
            drawings.Tracer.Visible = false
            drawings.TracerOutline.Visible = false
        end
        
        if Config.ESP.Names then
            drawings.Name.Text = player.Name
            drawings.Name.Position = Vector2.new(boxPos.X + boxWidth / 2, boxPos.Y - 18)
            drawings.Name.Color = Config.ESP.RainbowMode and Utility:RainbowColor(1.2) or Config.ESP.NameColor
            drawings.Name.Visible = true
        else
            drawings.Name.Visible = false
        end
        
        if Config.ESP.HealthBars then
            local healthPercent = humanoid.Health / humanoid.MaxHealth
            local barHeight = boxHeight * healthPercent
            local barWidth = 4
            local barPos = Vector2.new(boxPos.X - barWidth - 2, boxPos.Y + boxHeight - barHeight)
            
            drawings.HealthBarBG.Size = Vector2.new(barWidth, boxHeight)
            drawings.HealthBarBG.Position = Vector2.new(boxPos.X - barWidth - 2, boxPos.Y)
            drawings.HealthBarBG.Visible = true
            
            drawings.HealthBar.Size = Vector2.new(barWidth, barHeight)
            drawings.HealthBar.Position = barPos
            drawings.HealthBar.Color = Color3.fromRGB(255 * (1 - healthPercent), 255 * healthPercent, 0)
            drawings.HealthBar.Visible = true
        else
            drawings.HealthBar.Visible = false
            drawings.HealthBarBG.Visible = false
        end
        
        if Config.ESP.Distance then
            drawings.Distance.Text = math.floor(distance) .. "m"
            drawings.Distance.Position = Vector2.new(boxPos.X + boxWidth / 2, boxPos.Y + boxHeight + 5)
            drawings.Distance.Color = Config.ESP.RainbowMode and Utility:RainbowColor(0.6) or Config.ESP.DistanceColor
            drawings.Distance.Visible = true
        else
            drawings.Distance.Visible = false
        end
    end
end

