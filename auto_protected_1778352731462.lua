function GUI:SwitchTab(tabName)
    for name, btn in pairs(GUI.TabButtons) do
        if name == tabName then
            btn.BackgroundColor3 = Config.GUI.AccentColor
            btn.TextColor3 = Color3.fromRGB(255, 255, 255)
