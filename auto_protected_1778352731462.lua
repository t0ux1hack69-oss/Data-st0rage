-- AIMBOT LOGIC
local Aimbot = {}

function Aimbot:UpdateTarget()
    if tick() - State.LastTargetUpdate < State.TargetUpdateRate then
        return
    end
    State.LastTargetUpdate = tick()
    
    if State.IsLocked and State.LockedTarget then
        local character = Utility:GetCharacter(State.LockedTarget)
        if character and Utility:IsAlive(character) then
            if not Config.Aimbot.WallCheck or Utility:IsVisible(character:FindFirstChild(Config.Aimbot.TargetPart) or character:FindFirstChild("Head")) then
                local distance = Utility:GetDistance(Camera.CFrame.Position, (character:FindFirstChild("HumanoidRootPart") or character:FindFirstChild("Head")).Position)
                if distance <= Config.Aimbot.DistanceLimit then
                    State.CurrentTarget = State.LockedTarget
                    return
                end
            end
        end
    end
    
    if Config.Aimbot.AutoSwitch or not State.CurrentTarget then
        local closestPlayer = Utility:GetClosestPlayer()
        if closestPlayer then
            State.CurrentTarget = closestPlayer
        else
            State.CurrentTarget = nil
        end
    end
end

function Aimbot:AimAtTarget()
    if not Config.Aimbot.Enabled then return end
    if not State.CurrentTarget then return end
    
    local character = Utility:GetCharacter(State.CurrentTarget)
    if not character then return end
    
    local targetPart = character:FindFirstChild(Config.Aimbot.TargetPart)
    if not targetPart then
        targetPart = character:FindFirstChild("Head")
        if not targetPart then return end
    end
    
    local targetPos = Utility:PredictPosition(targetPart, State.CurrentTarget)
    local cameraCF = Camera.CFrame
    local targetCF = CFrame.new(cameraCF.Position, targetPos)
    
    local smoothness = Config.Aimbot.Smoothness
    
    if Config.Aimbot.AntiShake then
        smoothness = math.clamp(smoothness, 0.05, 0.5)
    end
    
    if Config.Aimbot.MobileOptimized and State.MobileMode then
        smoothness = math.clamp(smoothness * 1.5, 0.1, 0.8)
    end
    
    Camera.CFrame = Utility:LerpCFrame(cameraCF, targetCF, smoothness)
end

-- TRIGGERBOT LOGIC
local Triggerbot = {}
Triggerbot.LastTrigger = 0

function Triggerbot:Check()
    if not Config.Triggerbot.Enabled then return end
    
    local mousePos = Vector2.new(Mouse.X, Mouse.Y)
    local ray = Camera:ViewportPointToRay(mousePos.X, mousePos.Y)
    
    local raycastParams = RaycastParams.new()
    raycastParams.FilterDescendantsInstances = {LocalPlayer.Character}
    raycastParams.FilterType = Enum.RaycastFilterType.Blacklist
    
    local result = Workspace:Raycast(ray.Origin, ray.Direction * 1000, raycastParams)
    
    if result then
        local hitModel = result.Instance:FindFirstAncestorOfClass("Model")
        if hitModel then
            local hitPlayer = Players:GetPlayerFromCharacter(hitModel)
            if hitPlayer and hitPlayer ~= LocalPlayer then
                if not Config.Triggerbot.TeamCheck or not Utility:IsTeammate(hitPlayer) then
                    local currentTime = tick()
                    if currentTime - self.LastTrigger >= Config.Triggerbot.Delay / 1000 then
                        self.LastTrigger = currentTime
                        if mouse1click then
                            mouse1click()
                        end
                        SoundSystem:Hit()
                    end
                end
            end
        end
    end
end

-- SILENT AIM LOGIC
local SilentAim = {}

function SilentAim:GetTargetPosition()
    if not Config.SilentAim.Enabled then return nil end
    
    local target = Utility:GetClosestPlayerSilent()
    if not target then return nil end
    
    if math.random(1, 100) > Config.SilentAim.HitChance then
        return nil
    end
    
    local character = Utility:GetCharacter(target)
    if not character then return nil end
    
    local targetPart = character:FindFirstChild(Config.SilentAim.TargetPart)
    if not targetPart then
        targetPart = character:FindFirstChild("Head")
        if not targetPart then return nil end
    end
    
    if Config.SilentAim.VisibilityCheck and not Utility:IsVisible(targetPart) then
        return nil
    end
    
    return targetPart.Position
end

