    local playerList = Instance.new("Frame")
    playerList.Name = "PlayerList"
    playerList.Size = UDim2.new(1, -10, 0, 0)
    playerList.Position = UDim2.new(0, 5, 0, 40)
    playerList.BackgroundTransparency = 1
    playerList.BorderSizePixel = 0
    playerList.Visible = false
    playerList.Parent = frame

    local listLayout = Instance.new("UIListLayout")
    listLayout.Padding = UDim.new(0, 3)
    listLayout.SortOrder = Enum.SortOrder.LayoutOrder
    listLayout.Parent = playerList

    DropdownFrame = frame
    DropdownButton = dropdownBtn

    local function ClearList()
        for _, child in pairs(playerList:GetChildren()) do
            if child:IsA("TextButton") then
                child:Destroy()
            end
        end
    end

    local function UpdatePlayerList()
        ClearList()

        local allPlayers = Players:GetPlayers()
        local count = 0

        for _, plr in pairs(allPlayers) do
            if plr ~= player then
                count = count + 1

                local btn = Instance.new("TextButton")
                btn.Size = UDim2.new(1, 0, 0, 30)
                btn.BackgroundColor3 = Color3.fromRGB(45, 45, 60)
                btn.Text = plr.DisplayName .. " (@" .. plr.Name .. ")"
                btn.TextColor3 = Color3.fromRGB(220, 220, 220)
                btn.TextSize = 11
                btn.Font = Enum.Font.Gotham
                btn.AutoButtonColor = true
                btn.Parent = playerList

                local btnC = Instance.new("UICorner")
                btnC.CornerRadius = UDim.new(0, 4)
                btnC.Parent = btn

                btn.MouseButton1Click:Connect(function()
                    SelectedPlayer = plr
                    Config.TargetPlayer = plr
                    dropdownBtn.Text = "а№ҖаёҘаё·аёӯаёҒ: " .. plr.DisplayName

                    -- аёӣаёҙаё” Dropdown
                    DropdownOpen = false
                    playerList.Visible = false
                    frame.Size = UDim2.new(1, 0, 0, 42)
                    arrow.Text = "в–ј"

                    -- аёЈаёөа№ҖаёӢа№Үаё•аёӘаёөаё—аёёаёҒаёӣаёёа№ҲаёЎ
                    for _, b in pairs(playerList:GetChildren()) do
                        if b:IsA("TextButton") then
                            b.BackgroundColor3 = Color3.fromRGB(45, 45, 60)
                        end
                    end
                    btn.BackgroundColor3 = Color3.fromRGB(80, 150, 255)
                end)
            end
        end

        -- аёӣаёЈаёұаёҡаёӮаёҷаёІаё”аёҒаёЈаёӯаёҡ Dropdown
        if DropdownOpen then
            local listHeight = math.min(count * 33, 200)
            frame.Size = UDim2.new(1, 0, 0, 45 + listHeight)
            playerList.Size = UDim2.new(1, -10, 0, listHeight)
        end

        return count
    end

    dropdownBtn.MouseButton1Click:Connect(function()
        DropdownOpen = not DropdownOpen

        if DropdownOpen then
            local count = UpdatePlayerList()
            playerList.Visible = true
            arrow.Text = "в–І"
        else
            playerList.Visible = false
            frame.Size = UDim2.new(1, 0, 0, 42)
            arrow.Text = "в–ј"
        end
    end)

    -- аёӯаёұаёӣа№Җаё”аё•аёЈаёІаёўаёҠаё·а№Ҳаёӯ Real-time
    Players.PlayerAdded:Connect(function()
        if DropdownOpen then
            UpdatePlayerList()
        end
    end)

    Players.PlayerRemoving:Connect(function(removedPlr)
        if SelectedPlayer == removedPlr then
            SelectedPlayer = nil
            Config.TargetPlayer = nil
            dropdownBtn.Text = "а№ҖаёҘаё·аёӯаёҒаёңаё№а№үа№ҖаёҘа№Ҳаёҷ..."
        end
        if DropdownOpen then
            UpdatePlayerList()
        end
    end)

    return frame
end

