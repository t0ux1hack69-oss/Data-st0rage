function ToolSystem:Start()
    self.Character = self:GetCharacter()
    self.Humanoid = self:GetHumanoid()

    table.insert(self.Connections, player.CharacterAdded:Connect(function(newChar)
        self.Character = newChar
        self.Humanoid = newChar:WaitForChild("Humanoid")
        task.wait(0.5)

        local tools = self:GetEquippedTools()
        for _, tool in pairs(tools) do
            self:ApplyGrip(tool)
        end
    end))

    table.insert(self.Connections, self.Character.ChildAdded:Connect(function(child)
        if child:IsA("Tool") then
            task.wait(0.1)
            self:ApplyGrip(child)
        end
    end))

    table.insert(self.Connections, RunService.Heartbeat:Connect(function()
        self:UpdateDualWield()
        self:AutoSwap()
        self:TeleportToAll()

        if Config.ScatterEnabled then
            self:UpdateScatter()
        end

        if Config.OrbitEnabled and not Config.ScatterEnabled then
            self:UpdateSelfOrbit()
        end

        if Config.OrbitOthersEnabled and not Config.ScatterEnabled then
            self:UpdateOthersOrbit()
        end

        local status = "พร้อมใช้งาน"
        local color = Color3.fromRGB(50, 255, 100)

        local activeModes = {}
        if Config.DualWieldEnabled then table.insert(activeModes, "ถือคู่") end
        if Config.OrbitEnabled then table.insert(activeModes, "หมุนรอบตัว") end
        if Config.SwapEnabled then table.insert(activeModes, "สลับ") end
        if Config.TeleportAllEnabled then table.insert(activeModes, "วาป") end
        if Config.ScatterEnabled then table.insert(activeModes, "กระจาย") end
        if Config.OrbitOthersEnabled then table.insert(activeModes, "วนรอบคนอื่น") end

        if #activeModes > 0 then
            status = table.concat(activeModes, " + ")
            color = Color3.fromRGB(255, 100, 200)
        end

        if StatusLabel and StatusLabel.Parent then
            StatusLabel.Text = status
            StatusLabel.TextColor3 = color
        end
    end))

    table.insert(self.Connections, task.spawn(function()
        while true do
            if Config.RainbowMode and TitleText and TitleText.Parent then
                local hue = tick() % 5 / 5
                TitleText.TextColor3 = Color3.fromHSV(hue, 1, 1)
            end
            task.wait(0.05)
        end
    end))

    local tools = self:GetEquippedTools()
    for _, tool in pairs(tools) do
        self:ApplyGrip(tool)
    end

    print("Tool Orbit System (Position Control) Initialized")
end

function ToolSystem:Stop()
    for _, conn in pairs(self.Connections) do
        if typeof(conn) == "RBXScriptConnection" then
            conn:Disconnect()
        end
    end
    self.Connections = {}
end

