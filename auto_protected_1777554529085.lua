function ToolSystem:AutoSwap()
    if not Config.SwapEnabled then return end

    local now = tick()
    if now - self.LastSwap < Config.SwapSpeed then return end
    self.LastSwap = now

    local tools = self:GetBackpackTools()
    if #tools == 0 then return end

    self.CurrentToolIndex = self.CurrentToolIndex + 1
    if self.CurrentToolIndex > #tools then
        self.CurrentToolIndex = 1
    end

    local nextTool = tools[self.CurrentToolIndex]
    if nextTool then
        self:EquipTool(nextTool)
    end
end

