function ToolSystem:GetEquippedTools()
    local tools = {}
    local char = self:GetCharacter()
    for _, child in pairs(char:GetChildren()) do
        if child:IsA("Tool") then
            table.insert(tools, child)
        end
    end
    return tools
end

