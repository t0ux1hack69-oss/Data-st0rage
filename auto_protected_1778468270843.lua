-- рҹҢү BRIDGE LOGIC
-- в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ

local function UpdateBridge()
    if not State.Enabled then return end

    local hrp = HumanoidRootPart
    if not hrp then return end

    -- а№ҖаёҠа№Үаё„аё§а№ҲаёІаёҒаёіаёҘаёұаёҮаё•аёҒаё«аёЈаё·аёӯа№„аёЎа№Ҳ
    local velocity = hrp.Velocity
    State.IsFalling = velocity.Y < -10

    -- аё„аёіаёҷаё§аё“аё•аёіа№Ғаё«аёҷа№ҲаёҮаё—аёөа№Ҳаё•а№үаёӯаёҮаё§аёІаёҮ
    local positions = CalculatePlacePositions()

    -- аё§аёІаёҮаёҡаёҘа№ҮаёӯаёҒа№Ғаё•а№ҲаёҘаё°аё•аёіа№Ғаё«аёҷа№ҲаёҮ
    for _, data in ipairs(positions) do
        if not State.Enabled then break end

        local success, msg = PlaceBlockAt(data.Position)

        if success then
            -- аё§аёІаёҮаёӘаёіа№ҖаёЈа№ҮаёҲ
        elseif msg == "Cooldown" or msg == "Rate limited" then
            task.wait(0.01)
        end
    end

    -- аё–а№үаёІаёҒаёіаёҘаёұаёҮаё•аёҒ а№ғаё«а№үаё§аёІаёҮаёҡаёҘа№ҮаёӯаёҒа№ғаё•а№үа№Җаё—а№үаёІаё—аёұаёҷаё—аёө
    if State.IsFalling then
        local emergencyPos = hrp.Position + Vector3.new(0, Config.PlaceBelowOffset, 0)
        emergencyPos = SnapToGrid(emergencyPos)
        PlaceBlockAt(emergencyPos)
    end
