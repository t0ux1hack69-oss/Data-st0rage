    local hrp = HumanoidRootPart
    local pos = hrp and hrp.Position or Vector3.new(0, 0, 0)
    local dir = CalculateMovementDirection()

    StatusEnabled.Text = State.Enabled and "⚡ Status: ON 🔥" or "⚡ Status: OFF"
