-- аё«аёІ Held Block
local function GetHeldBlock()
    local char = LocalPlayer.Character
    if not char then return nil end

    local block = char:FindFirstChild("Block")
    if block and block:IsA("Tool") then
        return block
    end

    if Config.AutoEquip then
        local backpack = LocalPlayer:FindFirstChild("Backpack")
        if backpack then
            local tool = backpack:FindFirstChild("Block")
            if tool and tool:IsA("Tool") then
                local humanoid = char:FindFirstChildOfClass("Humanoid")
                if humanoid then
                    humanoid:EquipTool(tool)
                    task.wait(0.15)
                    return char:FindFirstChild("Block")
                end
            end
        end
    end

    return nil
end

-- аё„аёіаёҷаё§аё“аё—аёҙаёЁаё—аёІаёҮаёҒаёІаёЈа№Җаё„аёҘаё·а№Ҳаёӯаёҷаё—аёөа№Ҳ
local function CalculateMovementDirection()
    local hrp = HumanoidRootPart
    if not hrp then return Vector3.new(0, 0, -1) end

    local currentPos = hrp.Position
    local velocity = hrp.Velocity

    -- а№ғаёҠа№ү velocity аё–а№үаёІаёЎаёөаёҒаёІаёЈа№Җаё„аёҘаё·а№Ҳаёӯаёҷаё—аёөа№ҲаёһаёӯаёӘаёЎаё„аё§аёЈ
    if velocity.Magnitude > 0.5 then
        local dir = velocity.Unit
        -- аёҘаёҡа№ҒаёҒаёҷ Y а№Җаёһаё·а№Ҳаёӯа№ғаё«а№үа№Җаёӣа№Үаёҷаё—аёҙаёЁаё—аёІаёҮа№Ғаёҷаё§аёЈаёІаёҡ
        dir = Vector3.new(dir.X, 0, dir.Z).Unit
        if dir.Magnitude > 0.1 then
            State.LastVelocity = dir
            return dir
        end
    end

    -- аё–а№үаёІа№„аёЎа№Ҳа№Җаё„аёҘаё·а№Ҳаёӯаёҷаё—аёөа№Ҳ а№ғаёҠа№үаё—аёҙаёЁаё—аёІаёҮаёҘа№ҲаёІаёӘаёёаё” аё«аёЈаё·аёӯаё—аёҙаёЁаё—аёІаёҮаё—аёөа№Ҳаё«аёұаёҷа№„аёӣ
    if State.LastVelocity.Magnitude > 0.1 then
        return State.LastVelocity
    end

    -- а№ғаёҠа№үаё—аёҙаёЁаё—аёІаёҮаё—аёөа№Ҳаё•аёұаё§аёҘаё°аё„аёЈаё«аёұаёҷа№„аёӣ
    local lookDir = hrp.CFrame.LookVector
    return Vector3.new(lookDir.X, 0, lookDir.Z).Unit
end

-- аё„аёіаёҷаё§аё“аё•аёіа№Ғаё«аёҷа№ҲаёҮаёҡаёҷаёҒаёЈаёҙаё”
local function SnapToGrid(position)
    if not Config.GridSnap then return position end
    local size = Config.BlockSize
    return Vector3.new(
        math.round(position.X / size) * size,
        math.round(position.Y / size) * size,
        math.round(position.Z / size) * size
    )
end

-- аё„аёіаёҷаё§аё“аё•аёіа№Ғаё«аёҷа№ҲаёҮаё§аёІаёҮаёҡаёҘа№ҮаёӯаёҒ
local function CalculatePlacePositions()
    local hrp = HumanoidRootPart
    if not hrp then return {} end

    local positions = {}
    local currentPos = hrp.Position
    local direction = CalculateMovementDirection()
    local rightVector = Vector3.new(-direction.Z, 0, direction.X) -- а№ҒаёҒаёҷаёӮаё§аёІаёЎаё·аёӯ

    -- аё•аёіа№Ғаё«аёҷа№ҲаёҮаёҗаёІаёҷ (а№ғаё•а№үа№Җаё—а№үаёІ + offset)
    local baseHeight = currentPos.Y + Config.PlaceBelowOffset
    local basePos = Vector3.new(currentPos.X, baseHeight, currentPos.Z)
    basePos = SnapToGrid(basePos)

    -- аё„аёіаёҷаё§аё“аё•аёіа№Ғаё«аёҷа№ҲаёҮаёӘаёіаё«аёЈаёұаёҡа№Ғаё•а№ҲаёҘаё°аёҡаёҘа№ҮаёӯаёҒа№ғаёҷаё„аё§аёІаёЎаёҒаё§а№үаёІаёҮ
    local width = Config.BridgeWidth
    local halfWidth = math.floor(width / 2)

    for i = -halfWidth, halfWidth do
        -- аё•аёіа№Ғаё«аёҷа№ҲаёҮаёӮа№үаёІаёҮа№Ҷ (аё„аё§аёІаёЎаёҒаё§а№үаёІаёҮ)
        local sideOffset = rightVector * (i * Config.BlockSize)
        local sidePos = basePos + sideOffset

        -- аё§аёІаёҮаёҘа№Ҳаё§аёҮаё«аёҷа№үаёІаё•аёІаёЎаё—аёҙаёЁаё—аёІаёҮ
        for j = 0, Config.BridgeLength - 1 do
            local forwardOffset = direction * (j * Config.BlockSize)
            local placePos = sidePos + forwardOffset

            -- а№Җаёһаёҙа№ҲаёЎаё„аё§аёІаёЎаёӘаё№аёҮаё•аёІаёЎ Config
            placePos = placePos + Vector3.new(0, Config.BridgeHeight * Config.BlockSize, 0)

            table.insert(positions, {
                Position = placePos,
                Direction = direction,
                SideIndex = i,
                ForwardIndex = j,
            })
        end
    end

    return positions
end

-- а№ҖаёҠа№Үаё„аё§а№ҲаёІаё•аёіа№Ғаё«аёҷа№ҲаёҮаёҷаёөа№үаё§аёІаёҮа№„аёӣа№ҒаёҘа№үаё§аё«аёЈаё·аёӯаёўаёұаёҮ
local function IsPositionPlaced(position)
    local tolerance = Config.BlockSize * 0.5
    for _, placedPos in ipairs(State.PlacedPositions) do
        if (placedPos - position).Magnitude < tolerance then
            return true
        end
    end
    return false
end

