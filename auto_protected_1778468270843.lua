function ToggleBridge()
    State.Enabled = not State.Enabled

    if State.Enabled then
        -- а№ҖаёЈаёҙа№ҲаёЎаё—аёіаёҮаёІаёҷ
        ShowNotification("рҹҢү Auto Bridge ENABLED!", Color3.fromRGB(0, 255, 128), 2)
        ToggleButton.Text = "вҸ№ STOP BRIDGE"
        ToggleButton.BackgroundColor3 = Color3.fromRGB(255, 50, 50)
        MainStroke.Color = Color3.fromRGB(0, 255, 128)

        -- а№ҖаёЈаёҙа№ҲаёЎ loop
        if not State.BridgeCoroutine then
            State.BridgeCoroutine = task.spawn(function()
                while State.Enabled do
                    UpdateBridge()
                    task.wait(Config.CheckInterval)
                end
                State.BridgeCoroutine = nil
            end)
        end
