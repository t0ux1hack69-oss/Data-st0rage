-- หา Held Block
local function GetHeldBlock()
    local char = LocalPlayer.Character
    if not char then return nil end

    local block = char:FindFirstChild("Block")
    if block and block:IsA("Tool") then
        return block
    end

    if Config.AutoEquip then
        local backpack = LocalPlayer:FindFirstChild("Backpack")
        if backpack then
            local tool = backpack:FindFirstChild("Block")
            if tool and tool:IsA("Tool") then
                local humanoid = char:FindFirstChildOfClass("Humanoid")
                if humanoid then
                    humanoid:EquipTool(tool)
                    task.wait(0.15)
                    return char:FindFirstChild("Block")
                end
            end
        end
    end

    return nil
end

-- 🎯🎯🎯 หาบล็อกพื้นที่ใกล้เท้าที่สุด (สำคัญมาก!)
local function FindNearestGroundBlock()
    local hrp = HumanoidRootPart
    if not hrp then return nil end

    local playerPos = hrp.Position
    local nearestBlock = nil
    local nearestDistance = Config.SearchRadius

    -- ค้นหาใน BlocksFolder
    local blocksFolder = Config.BlocksFolder
    if not blocksFolder then return nil end

    -- วนลูปหาบล็อกใกล้สุด
    local function SearchInFolder(folder, currentDepth)
        if currentDepth > 3 then return end  -- จำกัดความลึกการค้นหา

        for _, child in ipairs(folder:GetChildren()) do
            if child:IsA("BasePart") then
                local distance = (child.Position - playerPos).Magnitude
                if distance < nearestDistance then
                    nearestDistance = distance
                    nearestBlock = child
                end
            elseif child:IsA("Folder") or child:IsA("Model") then
                SearchInFolder(child, currentDepth + 1)
            end
        end
    end

    SearchInFolder(blocksFolder, 0)

    -- ถ้าไม่เจอในรัศมีปกติ ขยายรัศมี
    if not nearestBlock then
        nearestDistance = Config.MaxSearchRadius
        local function ExtendedSearch(folder, currentDepth)
            if currentDepth > 5 then return end

            for _, child in ipairs(folder:GetChildren()) do
                if child:IsA("BasePart") then
                    local distance = (child.Position - playerPos).Magnitude
                    if distance < nearestDistance then
                        nearestDistance = distance
                        nearestBlock = child
                    end
                elseif child:IsA("Folder") or child:IsA("Model") then
                    ExtendedSearch(child, currentDepth + 1)
                end
            end
        end
        ExtendedSearch(blocksFolder, 0)
    end

    State.NearestBlock = nearestBlock
    return nearestBlock
end

-- คำนวณทิศทางการเคลื่อนที่
local function CalculateMovementDirection()
    local hrp = HumanoidRootPart
    if not hrp then return Vector3.new(0, 0, -1) end

    local velocity = hrp.Velocity

    -- ใช้ velocity ถ้ามีการเคลื่อนที่พอสมควร
    if velocity.Magnitude > 0.5 then
        local dir = velocity.Unit
        -- ลบแกน Y เพื่อให้เป็นทิศทางแนวราบ
        dir = Vector3.new(dir.X, 0, dir.Z).Unit
        if dir.Magnitude > 0.1 then
            State.LastVelocity = dir
            return dir
        end
    end

    -- ถ้าไม่เคลื่อนที่ ใช้ทิศทางล่าสุด หรือทิศทางที่หันไป
    if State.LastVelocity.Magnitude > 0.1 then
        return State.LastVelocity
    end

    -- ใช้ทิศทางที่ตัวละครหันไป
    local lookDir = hrp.CFrame.LookVector
    return Vector3.new(lookDir.X, 0, lookDir.Z).Unit
end

-- คำนวณตำแหน่งบนกริด
local function SnapToGrid(position)
    if not Config.GridSnap then return position end
    local size = Config.BlockSize
    return Vector3.new(
        math.round(position.X / size) * size,
        math.round(position.Y / size) * size,
        math.round(position.Z / size) * size
    )
end

-- 🎯🎯🎯 คำนวณตำแหน่งวางบล็อก (ต่อจากบล็อกพื้นที่ใกล้ที่สุด)
local function CalculatePlacePositions()
    local hrp = HumanoidRootPart
    if not hrp then return {} end

    -- หาบล็อกพื้นที่ใกล้ที่สุด
    local nearestBlock = FindNearestGroundBlock()
    if not nearestBlock then
        return {}  -- ไม่มีบล็อกพื้น ไม่วาง
    end

    local positions = {}
    local playerPos = hrp.Position
    local direction = CalculateMovementDirection()
    local rightVector = Vector3.new(-direction.Z, 0, direction.X) -- แกนขวามือ

    -- ใช้ตำแหน่งบล็อกพื้นเป็นจุดเริ่มต้น
    local basePos
    if nearestBlock:IsA("BasePart") then
        basePos = nearestBlock.Position
    else
        local part = nearestBlock:FindFirstChildWhichIsA("BasePart")
        if part then
            basePos = part.Position
        else
            return {}
        end
    end

    -- ปรับความสูงให้ตรงกับบล็อกพื้น (หรือใต้เท้า)
    local baseHeight = playerPos.Y + Config.PlaceBelowOffset
    basePos = Vector3.new(basePos.X, baseHeight, basePos.Z)
    basePos = SnapToGrid(basePos)

    -- คำนวณตำแหน่งสำหรับแต่ละบล็อกในความกว้าง
    local width = Config.BridgeWidth
    local halfWidth = math.floor(width / 2)

    for i = -halfWidth, halfWidth do
        -- ตำแหน่งข้างๆ (ความกว้าง)
        local sideOffset = rightVector * (i * Config.BlockSize)
        local sidePos = basePos + sideOffset

        -- วางล่วงหน้าตามทิศทาง
        for j = 0, Config.BridgeLength - 1 do
            local forwardOffset = direction * (j * Config.BlockSize)
            local placePos = sidePos + forwardOffset

            table.insert(positions, {
                Position = placePos,
                Direction = direction,
                SideIndex = i,
                ForwardIndex = j,
                TargetBlock = nearestBlock,  -- บล็อกเป้าหมายที่จะต่อ
            })
        end
    end

    return positions
end

-- เช็คว่าตำแหน่งนี้วางไปแล้วหรือยัง
local function IsPositionPlaced(position)
    local tolerance = Config.BlockSize * 0.5
    for _, placedPos in ipairs(State.PlacedPositions) do
        if (placedPos - position).Magnitude < tolerance then
            return true
        end
    end
    return false
end

-- เพิ่มตำแหน่งที่วางแล้ว
local function AddPlacedPosition(position)
    table.insert(State.PlacedPositions, position)
    -- ลบตำแหน่งเก่าถ้าเยอะเกินไป
    if #State.PlacedPositions > 500 then
        table.remove(State.PlacedPositions, 1)
    end
end

-- สุ่มดีเลย์
local function GetRandomizedDelay()
    if Config.RandomizeDelay then
        return Config.PlaceDelay + (math.random() * (Config.MaxRandomDelay - Config.MinRandomDelay) + Config.MinRandomDelay)
    end
    return Config.PlaceDelay
end

-- ═══════════════════════════════════════════════════════════════════════════════
-- 🚀 CORE PLACE FUNCTION (ต่อบล็อกจากบล็อกพื้นจริง)
-- ═══════════════════════════════════════════════════════════════════════════════

local function PlaceBlockAt(position, targetBlock)
    -- เช็ค cooldown
    local currentTime = tick()
    if currentTime - State.LastPlaceTime < GetRandomizedDelay() then
        return false, "Cooldown"
    end

    -- เช็ค rate limit
    if currentTime - State.SecondTimer >= 1 then
        State.SecondTimer = currentTime
        State.PlacedThisSecond = 0
    end
    if State.PlacedThisSecond >= Config.MaxPlacePerSecond then
        return false, "Rate limited"
    end

    -- หาบล็อกที่ถือ
    local heldBlock = GetHeldBlock()
    if not heldBlock then
        return false, "No block"
    end

    -- เช็คว่าวางตำแหน่งนี้ไปแล้วหรือยัง
    if IsPositionPlaced(position) then
        return false, "Already placed"
    end

    -- 🎯🎯🎯 ใช้บล็อกพื้นจริงเป็น Target (ไม่ใช่ HRP!)
    if not targetBlock then
        targetBlock = FindNearestGroundBlock()
    end

    if not targetBlock then
        return false, "No ground block found"
    end

    -- คำนวณ CFrame สำหรับตำแหน่งใหม่ (ต่อจากบล็อกพื้น)
    local targetCFrame = CFrame.new(position)

    -- คำนวณ Offset CFrame (ติดกับบล็อกพื้น)
    local offsetCFrame = CFrame.new(0, 0, 0, 1, 0, 0, 0, 1, 0, 0, 0, 1)

    -- สร้าง args
    local args = {
        [1] = targetBlock,        -- 🎯 บล็อกพื้นจริง! (ไม่ใช่ HRP)
        [2] = targetCFrame,      -- ตำแหน่งที่จะวางใหม่
        [3] = heldBlock,         -- บล็อกที่ถือ
        [4] = offsetCFrame,      -- offset
        [5] = BlockPlaceSound,   -- เสียง
        [6] = Config.BlockID,    -- block ID
    }

    -- ส่งรีโมท
    local success, err = pcall(function()
        PlaceBlockEvent:FireServer(unpack(args))
    end)

    if success then
        State.LastPlaceTime = currentTime
        State.PlacedCount = State.PlacedCount + 1
        State.PlacedThisSecond = State.PlacedThisSecond + 1
        AddPlacedPosition(position)
        return true, "Success"
    else
        return false, tostring(err)
    end
end

