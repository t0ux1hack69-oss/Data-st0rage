        MainStroke.Color = Color3.fromRGB(0, 255, 128)

        -- เริ่ม loop
        if not State.BridgeCoroutine then
            State.BridgeCoroutine = task.spawn(function()
                while State.Enabled do
                    UpdateBridge()
                    UpdateVisualizer()
                    task.wait(Config.CheckInterval)
                end
                State.BridgeCoroutine = nil
                ClearVisualizer()
            end)
        end
    else
        -- หยุด
        ShowNotification("⏹ Auto Bridge DISABLED", Color3.fromRGB(255, 80, 80), 2)
        ToggleButton.Text = "▶ START BRIDGE"
        ToggleButton.BackgroundColor3 = Color3.fromRGB(0, 200, 100)
